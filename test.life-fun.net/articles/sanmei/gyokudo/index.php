    <h2>玉堂星とは</h2>
    <p>算命学の人体図は、星の配置から個人の精神や才能を読み解くための図です。玉堂星は、知性と継承の本能を司る星です。「玉堂」とは玉で飾られた高貴な御堂を意味し、知識という宝を大切に守り伝えていくこの星の性質を表しています。</p>
    <div class="star-info">
      <span class="star-info-item"><strong>五行</strong>　水性（陰）</span>
      <span class="star-info-item"><strong>象徴</strong>　知性・学習・継承</span>
      <span class="star-info-item"><strong>キーワード</strong>　深さ・伝達・学術</span>
    </div>
    <p style="margin-top:1rem">五行では「水性（すいせい）」の「陰（いん）」に分類されます。自然界では、静かに地下深くに湛えられた泉に例えられます。表面は穏やかですが、その深部には膨大な知識と洞察が蓄えられています。この星の人は、学ぶことと伝えることを自然な喜びとして生きます。</p>
