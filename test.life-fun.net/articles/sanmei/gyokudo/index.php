  <section class="art-section" id="personality">
    <h2>基本性格</h2>
    <p>玉堂星を持つ人は、知的で落ち着いた雰囲気を持っています。表面的な知識では満足せず、物事の深いところまで理解したいという欲求が強くあります。学術・伝統・歴史・専門知識など、時間をかけて積み上げてきたものに価値を感じます。また、知っていることを他者に伝える喜びも大きく、教えることへの適性が高い傾向があります。</p>
    <p>一方で、急な変化や感情的な場面への対応に戸惑うことがあります。知性で状況を把握しようとするため、感情的なぶつかり合いより、冷静な対話を好みます。また、慎重で熟考するタイプのため、素早い判断が求められる場面ではペースが合わないこともあります。「少し時間をください」という一言で、大半の状況を丁寧に整理できます。</p>
  </section>
  <section class="art-section" id="love">
    <h2>恋愛運</h2>
    <p>玉堂星の恋愛は、知的なつながりを大切にします。会話が楽しい相手、互いに学び合える相手に強く惹かれます。感情の高低より、静かで落ち着いた関係の中に深い絆を見出します。</p>
    <p>衝動的な恋愛より、時間をかけて互いを理解し合う交際が向いています。相手の考え方・価値観・知識に対するリスペクトが愛情の土台になります。また、伝統や文化に理解がある相手、知性を大切にする相手との関係が長続きしやすいです。感情的な激しさより、穏やかで誠実な関係に安心を感じます。</p>
  </section>
  <section class="art-section" id="work">
    <h2>仕事運</h2>
    <p>玉堂星の人が最も力を発揮するのは、知識を深め伝える仕事です。教育・学術研究・コンサルタント・伝統工芸・出版・図書館司書など、専門性と継承が求められる職種で高い評価を得ます。年数とともに専門知識が積み上がるキャリアで、時間をかけて真価が輝く仕事が向いています。</p>
    <p>結果をすぐに求められる職場や、深く考える前に動くことが求められる環境より、じっくり取り組める仕事の方がこの星の強みを活かせます。「先生」「師匠」「専門家」として認められる立場に自然と収まっていく傾向があります。</p>
  </section>
  <section class="art-section" id="aisho">
    <h2>相性</h2>
    <p>玉堂星と比較的相性が良いのは、同じ水性を持つ「龍高星（りゅうこうせい）」です。知識への探求心は方向が違いますが、知を大切にする姿勢が共鳴します。龍高星が新しい知を開拓し、玉堂星がそれを深め継承するような関係が生まれます。</p>
    <p>深く考え、信念をもって生きる「貫索星（かんさくせい）」とも、互いの芯の深さで安定した信頼関係が築けます。工夫が必要なのは、行動スピードが速い「車騎星（しゃきせい）」との関係です。深く考える玉堂星と、まず動く車騎星はペースが対照的です。互いの強みを認め合うことで、行動力と知性が補い合える関係になります。</p>
  </section>
