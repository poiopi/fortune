    <p>細かいルールや管理体制が強い組織では窮屈に感じやすいです。フリーランスや起業など、自分のペースで動ける働き方が向いています。短期的に集中する力はありますが、長期の地道な継続より、プロジェクトベースで動く環境の方が本来の能力を引き出せます。好奇心の向く先を仕事にすることが、最大の成功につながります。</p>
