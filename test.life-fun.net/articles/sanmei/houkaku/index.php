    <p>裏を返すと、計画性や締め切りへの意識が課題になることもあります。「やればできる」と分かっていても、気が乗らないと手が止まりがちです。また、責任の重さを感じると、逃げ出したくなる衝動が生じることも。自分の「好き」という感覚を仕事や生活に結びつけることが、この星の才能を最大限に活かす鍵になります。</p>
