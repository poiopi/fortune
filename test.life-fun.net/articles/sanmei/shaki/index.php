    <h2>車騎星とは</h2>
    <p>算命学の人体図は、星の配置から個人の精神や才能を読み解くための図です。車騎星は、行動と突破の本能を司る星です。「車騎」とは騎兵が乗る戦車・軍馬を意味し、素早く前進し、障害を切り裂いていく力強さがこの星の本質です。</p>
    <div class="star-info">
      <span class="star-info-item"><strong>五行</strong>　金性（陽）</span>
      <span class="star-info-item"><strong>象徴</strong>　刃・突破・勝負</span>
      <span class="star-info-item"><strong>キーワード</strong>　行動力・スピード・勇気</span>
    </div>
    <p style="margin-top:1rem">五行では「金性（きんせい）」の「陽（よう）」に分類されます。自然界では鋭く研ぎ澄まされた刃に例えられます。迷わず進み、必要とあれば正面から突き破る。この星の人にとって、行動こそが思考であり、動いてから考えるのが本来のリズムです。</p>
  </section>
  <section class="art-section" id="personality">
    <h2>基本性格</h2>
    <p>車騎星を持つ人は、行動力とスピード感に長けています。考えているうちに動いている、というのがこの星のスタイルです。壁にぶつかっても怯まず、むしろそれを乗り越えることに喜びを感じます。瞬発力が高く、緊急事態や変化の速い状況でも素早く対応できます。</p>
    <p>一方で、せっかちな面が出ると、相手のペースや状況を確認せずに突き進んでしまうことがあります。思ったことをすぐ口に出すため、言葉がきつく受け取られることも。また、長期にわたる地道な作業より、目の前の課題に集中するスタイルが向いています。「まず動く」という本能は、適切なタイミングで発揮すれば大きな強みになります。</p>
  </section>
  <section class="art-section" id="love">
    <h2>恋愛運</h2>
    <p>車騎星の恋愛は、積極的で情熱的です。気になった相手にはアプローチを躊躇しません。言葉でも行動でも、愛情を真っ直ぐに伝えます。相手を引っ張っていくリーダーシップを自然と発揮し、デートの計画なども積極的に進めます。</p>
    <p>課題となるのは、相手のペースへの対応です。自分のテンポで動くため、慎重な性格の相手には「ちょっと速すぎる」と感じさせることがあります。また、感情的になりやすく、些細な衝突が大きくなることも。相手の気持ちに耳を傾け、一呼吸置く習慣が、恋愛関係をより豊かにします。</p>
  </section>
  <section class="art-section" id="work">
    <h2>仕事運</h2>
    <p>車騎星の人が輝くのは、行動力を直接評価される職場です。営業・スポーツ選手・警察・消防・救急・軍事・起業家など、スピードと突破力が求められる仕事で成果を上げます。変化の速い業界でも素早く対応でき、リスクを恐れずに先頭に立てるのが強みです。</p>
    <p>じっくり考えてから動くことが求められる研究・分析職より、結果が即座に見える仕事の方がモチベーションが続きます。プロジェクトの立ち上げや、課題を突破する局面で特に力を発揮します。組織の中では先陣を切る役割が向いており、後方支援より最前線に立つ仕事が本来の強みを引き出します。</p>
  </section>
  <section class="art-section" id="aisho">
    <h2>相性</h2>
    <p>車騎星と比較的相性が良いのは、温かく受け止めてくれる「禄存星（ろくそんせい）」です。車騎星の行動力と禄存星の包容力が組み合わさると、互いの強みを活かしながら安定した関係が生まれます。禄存星が車騎星の突進を包み込み、方向を整えてくれます。</p>
    <p>人をつなぐ力を持つ「石門星（せきもんせい）」とも、行動力とコーディネート力が補い合える関係です。工夫が必要なのは、繊細な感受性を持つ「調舒星（ちょうじょせい）」との関係です。車騎星のスピードと勢いは、調舒星の繊細さと衝突しやすいです。相手の内面に意識を向ける余裕を持つことが、関係を深める鍵になります。</p>
  </section>
