  <section class="art-section" id="personality">
    <h2>基本性格</h2>
    <p>龍高星を持つ人は、強烈な知的好奇心と探求心を持っています。新しいこと、未知のこと、常識の枠を超えたことに本能的に引き寄せられます。既存の答えに満足せず、「本当にそうなのか」と問い直し、自分で確かめずにはいられない性質があります。海外文化・先端技術・異分野への関心が高く、自ら未踏の領域に踏み込んでいきます。</p>
    <p>一方で、同じ場所に留まり続けることへの抵抗感があります。一つのことに夢中になっても、ある段階で急に関心が移ることがあります。「また変わった」と見られることもありますが、それはこの星の本来のリズムです。また、革新を重んじるため、慣習やルールを「なぜそうなのか」と問い直すクセが強く、保守的な環境で摩擦が生じることがあります。</p>
  </section>
  <section class="art-section" id="love">
    <h2>恋愛運</h2>
    <p>龍高星の恋愛は、知的な刺激と自由が鍵です。話していて刺激を受ける相手、新しい視点を与えてくれる相手に強く惹かれます。ルーティンな関係より、一緒にいるたびに新しい発見がある交際を好みます。</p>
    <p>束縛への抵抗感が強く、「いつも連絡して」「どこにいるか教えて」という管理型の関係は長続きしません。相手の自由を尊重し、自分の自由も尊重してもらえる関係が安定します。また、恋愛相手に成長や変化を求めるため、安定を重んじるタイプとは価値観のすり合わせに時間がかかることがあります。</p>
  </section>
  <section class="art-section" id="work">
    <h2>仕事運</h2>
    <p>龍高星の人が輝くのは、未知を開拓する仕事です。研究者・エンジニア・先端技術・海外ビジネス・起業家・コンサルタントなど、既存の枠を超えて新しい価値を生む仕事が向いています。「まだ答えがない問い」に挑む局面でこの星の本領が発揮されます。</p>
    <p>マニュアル通りにこなすことを求める職場や、変化を嫌う組織文化では持ち前の探求心が活かせません。変化が激しい業界、スタートアップ、研究機関など、常に新しいことを求められる環境の方がモチベーションが持続します。異文化・異業種との接点が多い仕事でも大きな力を発揮します。</p>
  </section>
  <section class="art-section" id="aisho">
    <h2>相性</h2>
    <p>龍高星と比較的相性が良いのは、自由な発想を持つ「鳳閣星（ほうかくせい）」です。お互いにルールより感性を大切にし、同じ方向に自由に動けるため、束縛感なく共鳴できる関係が生まれます。</p>
    <p>人と人をつなぐ力を持つ「石門星（せきもんせい）」とも、探求力とコーディネート力が補い合える関係です。龍高星が切り開いた新しい世界を、石門星が広げていくような組み合わせです。工夫が必要なのは、安定を求める「司禄星（しろくせい）」との関係です。変化への衝動と安定への欲求という対照的な性質を持つため、互いの価値観の違いを理解し尊重する意識が長続きの鍵になります。</p>
  </section>
