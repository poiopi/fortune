    <h2>龍高星とは</h2>
    <p>算命学の人体図は、星の配置から個人の精神や才能を読み解くための図です。龍高星は、探求と革新の本能を司る星です。「龍高」とは天空高く舞い上がる龍を意味し、地に足をつけたままでなく、常に高みへ、未知の領域へと向かっていく衝動がこの星の本質です。</p>
    <div class="star-info">
      <span class="star-info-item"><strong>五行</strong>　水性（陽）</span>
      <span class="star-info-item"><strong>象徴</strong>　探求・革新・異文化</span>
      <span class="star-info-item"><strong>キーワード</strong>　好奇心・型破り・開拓者</span>
    </div>
    <p style="margin-top:1rem">五行では「水性（すいせい）」の「陽（よう）」に分類されます。自然界では、湧き出て広がり続ける清流に例えられます。一か所に留まらず、次の未知の場所を目指して流れていく水の勢い。この星の人は、常に「次は何だろう」という問いとともに生きています。</p>
