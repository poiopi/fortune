      <h3>火のエレメントとは</h3>
      <p>火のエレメントを持つ星座（牡羊座・獅子座・射手座）は、直感的で情熱的、自発的に行動するエネルギーを持ちます。創造力が高く、周囲を鼓舞する力があります。一方で衝動的になりやすかったり、持続力に課題が出ることもあります。</p>
      <h3>活動宮（Cardinal）とは</h3>
      <p>活動宮は4つの季節の変わり目（春・夏・秋・冬の始まり）に対応するクオリティです。牡羊座・蟹座・天秤座・山羊座が該当します。活動宮の星座は「新しいことを始める」「変化を起こす」エネルギーが強く、リーダーシップを発揮しやすい傾向があります。</p>
