      <h2>牡羊座とは</h2>
      <p>牡羊座（おひつじざ）は、3月21日〜4月19日生まれの人が持つ太陽星座です。黄道十二宮の第1番目に位置し、春分点から始まる「星座サイクルの起点」とされています。</p>
      <p>西洋占星術では、牡羊座は火のエレメント（火・地・風・水の四元素の一つ）と活動宮（Cardinal）のクオリティを持ちます。「始める力」「切り開く力」が特に強く、何事にも真っ先に飛び込む先駆者的な気質が特徴です。</p>
      <p>守護星は火星（Mars）。古代ローマ神話の軍神マルスに対応するこの惑星は、行動・闘争心・情熱・推進力を司ります。この火星のエネルギーが牡羊座の積極的な性質を形作っています。</p>
      <div class="highlight-box">
        <p>牡羊座のキーワードは<strong>「開拓・情熱・スピード・独立」</strong>。12星座の中で最も「今すぐ動く」エネルギーを持つ星座です。</p>
      </div>
    </section>

