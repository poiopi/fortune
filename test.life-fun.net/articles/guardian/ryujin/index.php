    <h2>龍神とは</h2>
    <p>龍神（りゅうじん）は、日本神話・東アジアの霊的伝統において最も高位の存在のひとつとされる守護霊です。海・川・雨・風・雷を司り、天と地をつなぐ存在として古くから信仰されてきました。日本各地の神社には龍神を祀るものが多く、龍神は「変化」「浄化」「上昇」を象徴する神聖な霊として崇められています。</p>
    <p>守護霊としての龍神は、その人の魂に強い生命エネルギーを与え、困難を突破する力をもたらすとされます。龍神が守護霊につく人は、穏やかな日常よりも激しい変化の中でこそ真価を発揮するタイプが多いとされています。</p>
    <p>当サイトの守護霊診断では、龍神を「天龍」「水龍」「金龍」の3タイプに分類しています。それぞれの龍神は司る領域と与えるエネルギーが異なります。</p>
