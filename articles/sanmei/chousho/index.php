    <h2>調舒星とは</h2>
    <p>算命学の人体図は、星の配置から個人の精神や才能を読み解くための図です。調舒星は、感性と芸術性の本能を司る星です。「調べ」「舒べる」という字が示すように、美しいものを感じ取り、それを表現することに喜びを見出す性質を持ちます。</p>
    <div class="star-info">
      <span class="star-info-item"><strong>五行</strong>　火性（陰）</span>
      <span class="star-info-item"><strong>象徴</strong>　感性・孤高・芸術</span>
      <span class="star-info-item"><strong>キーワード</strong>　繊細・完璧主義・深い感受性</span>
    </div>
    <p style="margin-top:1rem">五行では「火性（かせい）」の「陰（いん）」に分類されます。自然界では温かくも繊細に燃える細い炎のような存在です。明るく大きく燃え上がる鳳閣星（火性の陽）とは対照的に、内側に向かって深く静かに燃える炎のように、自分の内面世界を大切にする傾向があります。</p>
  </section>
  <section class="art-section" id="personality">
    <h2>基本性格</h2>
    <p>調舒星を持つ人は、鋭い感受性と深い内面世界を持ちます。美しいものへの強いこだわりがあり、中途半端や妥協を嫌う完璧主義者です。普段は物静かに見えても、心の中では豊かな感情と思考が常に動いています。他の人が気づかない細部の美しさや、言葉の裏にある感情を読み取る洞察力に長けています。</p>
    <p>一方で、感受性の高さゆえに傷つきやすく、不快な環境でエネルギーを大きく消耗することがあります。人との関わりでも独自の距離感を保つことを大切にし、誰でも気軽に近づけるわけではありません。理想が高いため、現実とのギャップに苦しむ場面もあります。しかし、その高い理想こそが、深みのある作品や仕事を生み出す原動力になります。</p>
  </section>
  <section class="art-section" id="love">
    <h2>恋愛運</h2>
    <p>調舒星の恋愛は、表面的なお付き合いより魂の共鳴を求めます。外見や社会的な条件よりも、話していて「この人は分かってくれる」と感じられるかどうかを重視します。信頼できると感じた相手には、純粋で深い愛情を一途に注ぎます。</p>
    <p>繊細なため、言葉の端々や態度の微妙な変化に敏感です。相手の何気ない一言が深く傷つく原因になることもあります。感情を言葉にして伝えることが得意でない面もあり、「なぜ分かってくれないのか」と内心もどかしさを感じることもあるでしょう。相手に自分の内面を少しずつ開示していく勇気が、この星の恋愛をより豊かにします。</p>
  </section>
  <section class="art-section" id="work">
    <h2>仕事運</h2>
    <p>調舒星の人が才能を最大限に発揮できるのは、感性を活かせる創造的な仕事です。アーティスト・作家・デザイナー・写真家・音楽家・詩人など、自分の感性を直接表現できる職種で評価を得ます。繊細な観察眼と、「これではない」という審美感覚が、高品質な作品を生み出す土台になります。</p>
    <p>集団の中での人間関係や、感性と無関係な事務作業は消耗しやすい傾向があります。自分の感覚を信じながら一人で深めていける環境が最も向いています。評価されるまでに時間がかかることもありますが、妥協せず積み重ねたものは、長く価値を保ちます。</p>
  </section>
  <section class="art-section" id="aisho">
    <h2>相性</h2>
    <p>調舒星と比較的相性が良いのは、同じ火性に属する「鳳閣星（ほうかくせい）」です。自由な表現を追い求める鳳閣星と、深い感性を磨く調舒星は、芸術的な方向性は違えど互いの本物への情熱を理解し合えます。</p>
    <p>知識と伝達力を持つ「玉堂星（ぎょくどうせい）」とも、落ち着いた知的な対話ができます。工夫が必要なのは、行動とスピードを重んじる「車騎星（しゃきせい）」との関係です。調舒星の繊細さと車騎星の勢いは真逆に映ることが多く、互いのペースと感受性を尊重することが関係の鍵になります。</p>
  </section>
