    <h2>牽牛星とは</h2>
    <p>算命学の人体図は、星の配置から個人の精神や才能を読み解くための図です。牽牛星は、誠実と品格の本能を司る星です。「牽牛」とは七夕の彦星を指し、誠実に働き続ける姿がこの星の本質を象徴しています。</p>
    <div class="star-info">
      <span class="star-info-item"><strong>五行</strong>　金性（陰）</span>
      <span class="star-info-item"><strong>象徴</strong>　品格・誠実・完全</span>
      <span class="star-info-item"><strong>キーワード</strong>　責任感・プライド・高い基準</span>
    </div>
    <p style="margin-top:1rem">五行では「金性（きんせい）」の「陰（いん）」に分類されます。自然界では丁寧に磨き上げられた宝石に例えられます。荒削りのままではなく、時間をかけて磨かれることで輝きを増す。この星の人は、自分自身をそのように高め続けることに価値を見出します。</p>
