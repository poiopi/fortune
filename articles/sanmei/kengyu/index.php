  <section class="art-section" id="personality">
    <h2>基本性格</h2>
    <p>牽牛星を持つ人は、誠実で責任感が強い性格です。一度引き受けたことは最後まで丁寧にやり遂げます。高い基準を自分に課すため、仕事でも人間関係でも質の高さにこだわります。社会的な評価や品格を重んじ、礼儀や作法を大切にする姿勢が自然と身についています。</p>
    <p>一方で、完璧を求めるがゆえに、人前での失敗を極度に嫌います。ミスを認めることに抵抗があり、プライドが傷つく場面でストレスを感じやすいです。また、他者にも高い基準を求めてしまうことがあり、「こうあるべき」という基準が周囲との摩擦になることもあります。自分への厳しさと同様に、他者への寛容さも意識することで、より豊かな人間関係が生まれます。</p>
  </section>
  <section class="art-section" id="love">
    <h2>恋愛運</h2>
    <p>牽牛星の恋愛は、誠実さを最重視します。軽い気持ちでの交際より、真剣に向き合える相手との真摯な関係を求めます。外見や流行より、品格と誠実さを持った相手に惹かれます。交際を決めるまでに時間をかけ、相手をよく見極めてから関係を深めます。</p>
    <p>一度交際が始まると、誠実で一途な愛情を注ぎます。約束を守り、記念日を大切にし、礼儀ある付き合いを維持します。ただし、完璧な関係を求めるがゆえに、相手の小さな言動が気になることもあります。相手への期待値を少し緩める意識が、より温かな関係につながります。</p>
  </section>
  <section class="art-section" id="work">
    <h2>仕事運</h2>
    <p>牽牛星の人が最も力を発揮するのは、責任と正確さが求められる仕事です。公務員・法律家・会計士・品質管理・管理職など、高い基準を維持し続けることが評価される職種で安定した成果を上げます。組織のルールや手順を丁寧に守り、周囲からの信頼が厚いです。</p>
    <p>曖昧な指示や、品質より速さを求める環境には不満を感じやすい傾向があります。「どうせこの程度でいい」という妥協を求める職場は向いていません。自分の誠実さと完全さへのこだわりが正当に評価される環境で、この星の真価は発揮されます。</p>
  </section>
  <section class="art-section" id="aisho">
    <h2>相性</h2>
    <p>牽牛星と比較的相性が良いのは、同じく地道な積み上げを大切にする「司禄星（しろくせい）」です。誠実さと堅実さが共鳴し、安定した信頼関係が自然と生まれます。互いに約束を守り、長期的に信頼を積み重ねていける相性です。</p>
    <p>信念を持って生きる「貫索星（かんさくせい）」とも、互いの芯の強さを認め合える関係が築けます。工夫が必要なのは、自由と遊び心を大切にする「鳳閣星（ほうかくせい）」との関係です。牽牛星の基準の高さと鳳閣星のマイペースさは対照的になりやすく、互いの価値観を尊重する意識が関係の鍵です。</p>
  </section>
