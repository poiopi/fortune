    <h2>司禄星とは</h2>
    <p>算命学の人体図は、星の配置から個人の精神や才能を読み解くための図です。司禄星は、蓄積と継続の本能を司る星です。「司」は管理・運営を、「禄」は豊かさ・財産を意味し、コツコツと積み重ねることで豊かさを築いていくこの星の性質を表しています。</p>
    <div class="star-info">
      <span class="star-info-item"><strong>五行</strong>　土性（陰）</span>
      <span class="star-info-item"><strong>象徴</strong>　蓄積・安定・継続</span>
      <span class="star-info-item"><strong>キーワード</strong>　堅実・慎重・コツコツ型</span>
    </div>
    <p style="margin-top:1rem">五行では「土性（どせい）」の「陰（いん）」に分類されます。自然界では、ゆっくりと豊かさを蓄える大地の深層に例えられます。急いで高く積み上げるより、一段一段確実に基盤を固めていく姿勢が、この星の最大の持ち味です。</p>
