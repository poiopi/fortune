    <h2>基本性格</h2>
    <p>司禄星を持つ人は、慎重で着実な生き方を好みます。目標を定めたら、焦らずに地道に向かっていく粘り強さがあります。変化より安定を重んじ、先の見通しが立たないことには慎重に動きます。約束を守り、地道な積み重ねを大切にするため、周囲から信頼される存在になりやすい傾向があります。</p>
    <p>一方で、変化の多い環境や予期しない出来事への対応に時間がかかることがあります。新しいことへの挑戦より、慣れ親しんだやり方を守る安心感を選ぶ傾向があります。また、貯蓄や資産形成への意識が高く、将来への備えを怠りません。「石橋を叩いて渡る」タイプですが、その慎重さが長期的には確かな結果につながります。</p>
  </section>
  <section class="art-section" id="love">
    <h2>恋愛運</h2>
    <p>司禄星の恋愛は、安定と誠実さを何より大切にします。一時の情熱より、長く信頼できる関係を築くことに価値を見出します。交際の初期から結婚・家庭という将来を意識し、相手との共同生活のリアルを冷静に考えられます。</p>
    <p>急な感情の変化や、感情的なぶつかり合いは苦手な傾向があります。穏やかに、丁寧に時間をかけて関係を育むことで、深い信頼関係が生まれます。一度決めた相手への誠実さは長く続き、結婚後も家庭を大切に守り続けます。</p>
  </section>
  <section class="art-section" id="work">
    <h2>仕事運</h2>
    <p>司禄星の人が力を発揮するのは、継続的な積み上げが評価される仕事です。経理・会計・総務・在庫管理・品質管理など、正確さとルーティンの継続が求められる職種で高い成果を上げます。長期にわたるプロジェクトや、積み重ねた知識が活きる専門職でも安定した評価を得ます。</p>
    <p>目まぐるしく変わるトレンドを追う仕事や、毎回イチから発想を求められるクリエイティブ職より、蓄積が財産になる仕事の方がこの星の強みを活かせます。地道な努力を継続することで、年数とともに評価が積み上がっていくキャリアが向いています。</p>
