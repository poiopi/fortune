  <section class="art-section" id="aisho">
    <h2>相性</h2>
    <p>司禄星と比較的相性が良いのは、同じ土性に属する「禄存星（ろくそんせい）」です。禄存星の温かな包容力と、司禄星の堅実さが組み合わさると、安心感のある長続きする関係が生まれます。互いに安定を大切にする価値観が一致しています。</p>
    <p>自立した個人として互いを尊重できる「貫索星（かんさくせい）」とも、刺激を与え合いながら安定した関係が築けます。工夫が必要なのは、変化を好む「龍高星（りゅうこうせい）」との関係です。安定を求める司禄星と、変化・探求を求める龍高星は方向性が違います。互いの価値観の違いを認識し、ゆったりとした関係を保つことが長続きの秘訣です。</p>
