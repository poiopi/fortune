    <h2>基本性格</h2>
    <p>石門星を持つ人は、社交的で誰とでも自然に打ち解ける明るさを持ちます。初対面でも相手の緊張をほぐし、場の雰囲気を和らげる能力に長けています。グループの中での立ち回りが上手く、仲間を大切にする誠実さがあります。人と人の間に橋をかけ、対立を調和に変えていくのがこの星の本質的な強みです。</p>
    <p>一方で、全方向への気配りが「八方美人」と誤解されることもあります。誰かと対立することを避けるあまり、自分の本音を引っ込めてしまいがちです。また、一人の時間を確保することが苦手で、人間関係に振り回されてエネルギーを消耗することも少なくありません。「自分はどうしたいか」を意識的に問い直すことが、この星の成長につながります。</p>
  </section>
  <section class="art-section" id="love">
    <h2>恋愛運</h2>
    <p>石門星の恋愛は、友人関係や共通のグループから自然と発展するケースが多いです。共に過ごす時間の中で信頼を深め、気づけば恋愛へと変わっていく展開を好みます。相手の気持ちや状況を細かく読み取る共感力が高いため、交際相手からは「よく分かってくれる」と感じてもらえることが多いでしょう。</p>
    <p>課題となるのは、相手の気持ちを優先するあまり、自分の本音を伝えにくい点です。「どうしたい？」と聞かれても「あなたに合わせる」とつい答えてしまう傾向があります。長く付き合うためには、自分の意見や希望を言葉にして伝える勇気が大切です。結婚後も仲間を大切にする社交的な姿勢は変わらず、家庭と友人関係の両立を自然とこなします。</p>
