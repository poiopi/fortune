    <p>禄存星の人が才能を発揮するのは、人の役に立てる仕事です。福祉・医療・介護・教育・サービス業・カウンセリングなど、誰かを直接支える職種で高い満足感と成果を得られます。相手のニーズを細かく読み取る能力が高く、後輩の育成やチームの精神的な支柱としても力を発揮します。</p>
    <p>成果よりプロセスを大切にする性質があるため、数字や競争を重視する職場より、人の成長や変化を実感できる環境の方が長続きします。自分が「誰かの役に立っている」と感じられることがモチベーションの源なので、その実感を持てる仕事環境を選ぶことが大切です。</p>
  </section>
  <section class="art-section" id="aisho">
    <h2>相性</h2>
    <p>禄存星と比較的相性が良いのは、同じ土性に属する「司禄星（しろくせい）」です。禄存星の包容力と、司禄星の堅実さが組み合わさると、安定した温かい関係が自然と生まれます。互いに与え合う姿勢が一致しており、長期的に信頼を深めやすい相性です。</p>
    <p>「石門星（せきもんせい）」とも、人を大切にするという共通の価値観から、温かいつながりが生まれます。工夫が必要なのは、自立心の強い「貫索星（かんさくせい）」との関係です。禄存星が尽くしすぎると、貫索星が距離を取ろうとすることがあります。相手の自立のペースを尊重することが、良い関係を保つ鍵です。</p>
