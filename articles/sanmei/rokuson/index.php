    <h2>基本性格</h2>
    <p>禄存星を持つ人は、温かく包容力にあふれています。困っている人を見ると放っておけない性格で、気づけば誰かの相談に乗っている、というシーンが日常に多くあります。分け隔てなく人に接するため、様々な人から慕われ、いつの間にか周囲に人が集まる存在になります。</p>
    <p>一方で、人に尽くしすぎて自分のエネルギーを消耗しやすい面があります。断ることが苦手で、頼まれると「ノー」と言えないまま抱え込んでしまうことも少なくありません。自分の限界を認識し、意識的に休息を取ることが、長く人の支えでいられる秘訣です。</p>
  </section>
  <section class="art-section" id="love">
    <h2>恋愛運</h2>
    <p>禄存星の恋愛は、相手を深く思いやり、包み込む愛情が特徴です。相手の悩みや弱さをそのまま受け止める包容力が、交際相手にとっての大きな安心感となります。恋人が落ち込んでいるとき、自然と寄り添い、そっと支える姿は多くの人の心を動かします。</p>
    <p>注意したいのは、与えすぎて自分の気持ちを後回しにしてしまうことです。「相手のため」という気持ちから自分を犠牲にし続けると、いつかその関係自体が重荷に感じられることがあります。自分が受け取ることへの遠慮を少し手放し、相手からの愛情も素直に受け入れることが、長く続く関係の土台になります。</p>
  </section>
