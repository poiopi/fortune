    <h2>禄存星とは</h2>
    <p>算命学の人体図は、星の配置から個人の精神や才能を読み解くための図です。禄存星は、奉仕と包容の本能を司る星です。「禄」は豊かさ・恵みを、「存」は存在・維持を意味し、人に恵みを与え、周囲を豊かにする星の性質をよく表しています。</p>
    <div class="star-info">
      <span class="star-info-item"><strong>五行</strong>　土性（陽）</span>
