    <p>天赦日は中国の陰陽五行説に由来する暦注で、季節（立春・立夏・立秋・立冬）と干支の組み合わせによって算出されます。「天がすべての罪を赦し、何をしても許される」という考え方から、この日に行うことは万事において成功するとされてきました。</p>
